func Test_cellS_apply(t *testing.T) {
	type fields struct {
		alive     bool
		nextState bool
	}
	tests := []struct {
		name   string
		fields fields
		want   bool
	}{
		{"alive + alive > alive + false", fields{true, true}, true},
		{"alive + alive > alive + false", fields{true, false}, false},
		{"alive + alive > alive + false", fields{false, true}, true},
		{"alive + alive > alive + false", fields{false, false}, false},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			c := &cellS{
				alive:     tt.fields.alive,
				nextState: tt.fields.nextState,
			}

			if c.apply(); c.alive != tt.want {
				t.Errorf("apply(), c.alive:%v, want %v", c.alive, tt.want)
			}
		})
	}
}

