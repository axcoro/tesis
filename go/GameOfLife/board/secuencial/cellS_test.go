func initBoard(_case []byte) (b BoardS) {

	b.Init(3, 3, 0, false)

	for index, row := range _case {

		alive := (row == 'x') // ' ' > false, 0 > es lo que voy a testear
		x, y := 0, 0

		switch index {
		case 0:
			x, y = 0, 0
		case 1:
			x, y = 0, 1
		case 2:
			x, y = 0, 2
		case 3:
			x, y = 1, 0
		case 4:
			x, y = 1, 1
		case 5:
			x, y = 1, 2
		case 6:
			x, y = 2, 0
		case 7:
			x, y = 2, 1
		case 8:
			x, y = 2, 2
		}

		b.getCell(x, y).alive = alive

	}

	return
}

