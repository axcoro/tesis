func Test_cellW_apply(t *testing.T) {
	type fields struct {
		alive     bool
		nextState bool
	}
	tests := []struct {
		name   string
		fields fields
		want   bool
	}{
		{"alive + alive > alive + false", fields{true, true}, true},
		{"alive + alive > alive + false", fields{true, false}, false},
		{"alive + alive > alive + false", fields{false, true}, true},
		{"alive + alive > alive + false", fields{false, false}, false},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			c := &cellW{
				alive:     tt.fields.alive,
				nextState: tt.fields.nextState,
			}

			if c.apply(); c.alive != tt.want {
				t.Errorf("apply(), c.alive:%v, want %v", c.alive, tt.want)
			}
		})
	}
}

func Test_cellW_next(t *testing.T) {
	type fields struct {
		alive     bool
		nextState bool
		board     *BoardW
	}
	type args struct {
		x int
		y int
	}

	type testCase struct {
		name   string
		fields fields
		args   args
		want   cellW
	}

	var parseCases = func() (_cases []testCase) {

		for _, _case := range cases {

			b, c, x, y := initBoard(_case)

			neighbors := [8]*cellW{b.getCell(x-1, y-1), b.getCell(x, y-1), b.getCell(x+1, y-1), b.getCell(x-1, y) /*b.getCell(x,y]*/, b.getCell(x+1, y), b.getCell(x-1, y+1), b.getCell(x, y+1), b.getCell(x+1, y+1)}
			alive := 0
			for _, neighbor := range neighbors {
				if neighbor != nil && neighbor.alive {
					alive = alive + 1
				}
			}

			_c := testCase{
				name: string(_case),
				fields: fields{
					alive:     c.alive,
					nextState: c.nextState,
					board:     c.board,
				},
				args: args{
					x: x,
					y: y,
				},
				want: cellW{
					alive:     c.alive,
					nextState: (alive == 3) || (alive == 2 && c.alive),
				},
			}

			_cases = append(_cases, _c)
		}

		return _cases
	}

	tests := parseCases()

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			c := &cellW{
				alive:     tt.fields.alive,
				nextState: tt.fields.nextState,
				board:     tt.fields.board,
			}

			got := c.next(tt.args.x, tt.args.y)
			got.board = nil

			if !reflect.DeepEqual(*got, tt.want) {
				t.Errorf("cellW.next() = %v, want %v", got, tt.want)
			}
		})
	}
}

func initBoard(_case []byte) (b *BoardW, c *cellW, _x, _y int) {

	b = &BoardW{}
	b.Init(3, 3, 0, 0, false)

	for index, row := range _case {

		alive := (row == 'x') // ' ' > false, 0 > es lo que voy a testear
		x, y := 0, 0

		switch index {
		case 0:
			x, y = 0, 0
		case 1:
			x, y = 0, 1
		case 2:
			x, y = 0, 2
		case 3:
			x, y = 1, 0
		case 4:
			x, y = 1, 1
		case 5:
			x, y = 1, 2
		case 6:
			x, y = 2, 0
		case 7:
			x, y = 2, 1
		case 8:
			x, y = 2, 2
		}

		cell := b.getCell(x, y)
		cell.alive = alive

		if row == '0' {
			c = cell
			_x = x
			_y = y
		}

	}

	return
}

